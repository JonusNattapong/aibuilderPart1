import csv
import uuid
import os

# ตรวจสอบและสร้างไดเรกทอรี DataOutput หากยังไม่มี
output_dir = 'DataOutput'
if not os.path.exists(output_dir):
    os.makedirs(output_dir)

# ตัวอย่างข้อความสำหรับ STT Transcripts
stt_texts = [
    # Conversations
    "A: สวัสดีครับคุณสมชาย สบายดีไหมครับ B: สบายดีครับคุณสมหญิง แล้วคุณล่ะครับ",
    "A: เย็นนี้ไปกินข้าวกันไหม B: ไปสิ อยากกินอะไรเป็นพิเศษหรือเปล่า",
    "A: ขอโทษนะครับ ห้องประชุมไปทางไหนครับ B: เดินตรงไปแล้วเลี้ยวซ้ายครับ ห้องอยู่สุดทางเดิน",
    "A: ตกลงเราจะเริ่มโปรเจกต์นี้กันเมื่อไหร่ B: น่าจะประมาณสัปดาห์หน้านะครับ ต้องรออนุมัติงบประมาณก่อน",
    "A: ลูกค้าสอบถามเรื่องสถานะการจัดส่งสินค้าครับ B: รบกวนขอหมายเลขคำสั่งซื้อด้วยค่ะ เดี๋ยวตรวจสอบให้",
    # Speech in different environments
    "(เสียงประกาศในสนามบิน) ผู้โดยสารโปรดทราบ เที่ยวบิน TG101 กรุงเทพฯ-เชียงใหม่ ประตูขึ้นเครื่องหมายเลข 5",
    "(เสียงในที่ประชุม) จากข้อมูลที่เราได้รวบรวมมา พบว่ายอดขายในไตรมาสที่ผ่านมาเติบโตขึ้นสิบห้าเปอร์เซ็นต์",
    "(เสียงในร้านกาแฟ) รับอเมริกาโน่เย็นแก้วหนึ่งครับ ไม่หวานเลย",
    "(เสียงบรรยายสารคดี) ป่าอเมซอนเป็นป่าดิบชื้นที่ใหญ่ที่สุดในโลก เป็นแหล่งอาศัยของสิ่งมีชีวิตหลากหลายสายพันธุ์",
    "(เสียงโทรศัพท์) สวัสดีค่ะ บริษัท ABC ยินดีให้บริการค่ะ ไม่ทราบว่าติดต่อเรื่องอะไรคะ",
    "(เสียงในห้องเรียน) นักเรียนทุกคน เปิดหนังสือไปที่หน้าห้าสิบสามนะครับ วันนี้เราจะเรียนเรื่อง...",
    "(เสียงในตลาด) ลดอีกหน่อยได้ไหมแม่ค้า ถุงนี้ร้อยเดียวได้เปล่า",
    "(เสียงรายงานข่าว) ขณะนี้เวลาสิบสองนาฬิกาสามสิบนาที ติดตามข่าวภาคเที่ยงกับดิฉัน...",
    "(เสียงในโรงพยาบาล) เชิญคุณสมศรี ใจดี ที่ช่องหมายเลขสามค่ะ",
    "(เสียงพากย์กีฬา) โอ้โห! ยิงเข้าประตูไปอย่างสวยงามครับ ขึ้นนำไปแล้วหนึ่งประตูต่อศูนย์",
    "(เสียงในรถไฟฟ้า) สถานีต่อไป สยาม ผู้โดยสารสามารถเปลี่ยนเส้นทางไปสายสีลมได้ที่สถานีนี้",
    "(เสียงสั่งอาหาร) เอาผัดกะเพราหมูสับไข่ดาวจานนึงครับ ขอเผ็ดๆ",
    "(เสียงสนทนาในที่ทำงาน) ไฟล์งานที่ส่งไปให้เมื่อเช้า ได้รับหรือยังครับ",
    "(เสียงประกาศ) ขอความร่วมมือทุกท่านงดใช้เสียงดังในบริเวณนี้",
    "(เสียงเล่าเรื่อง) กาลครั้งหนึ่งนานมาแล้ว มีชายชราคนหนึ่งอาศัยอยู่ตามลำพังในกระท่อมเล็กๆ กลางป่า",
    # เพิ่มเติมตัวอย่างตามต้องการ
]

# สร้างรายการข้อมูลพร้อม ID
rows = []
for text in stt_texts:
    rows.append([str(uuid.uuid4()), text])

# บันทึกเป็นไฟล์ CSV ในโฟลเดอร์ DataOutput
output_file = os.path.join(output_dir, 'thai_dataset_stt_transcript.csv')
with open(output_file, 'w', newline='', encoding='utf-8') as f:
    writer = csv.writer(f)
    writer.writerow(['id', 'text'])
    writer.writerows(rows)

print(f"Created {output_file}")
